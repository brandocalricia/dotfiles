    monitor =
    text = cmd[update:1000] date "+%I:%M %p"
    color = rgba(${FG_BRIGHT}ff)
    font_size = 110
    font_family = JetBrainsMono NF ExtraBold
    position = 0, 120
    halign = center
    valign = center
}
label {
    monitor =
    text = cmd[update:60000] date "+%A, %B %d"
    color = rgba(${ACCENT_PRIMARY}ff)
    font_size = 22
    font_family = JetBrainsMono Nerd Font
    position = 0, 30
    halign = center
    valign = center
}
label {
    monitor =
    text = \$USER
    color = rgba(${FG_DIM}e6)
    font_size = 16
    font_family = JetBrainsMono Nerd Font
    position = 0, -240
    halign = center
    valign = center
}
EOF
fi

# ── Fix GTK CSS alpha + restart waybar ───────────────────────────
if [[ "$(uname)" == "Linux" ]]; then
    sed -i "s/background: #\([0-9a-fA-F]\{6\}\)ee;/background: alpha(#\1, 0.93);/g" "$HOME/.config/waybar/style.css"
    sed -i "s/background: #\([0-9a-fA-F]\{6\}\)99;/background: alpha(#\1, 0.60);/g" "$HOME/.config/waybar/style.css"
    sed -i "s/background: #\([0-9a-fA-F]\{6\}\)fa;/background: alpha(#\1, 0.98);/g" "$HOME/.config/waybar/style.css"
    pkill waybar 2>/dev/null; sleep 1 && waybar &>/dev/null & disown
fi

# ── Mako notifications ────────────────────────────────────────────
if [[ "$(uname)" == "Linux" ]]; then
    mkdir -p "$HOME/.config/mako"
    cat > "$HOME/.config/mako/config" << EOF
background-color=#${BG_DARK}ff
text-color=#${FG_BRIGHT}ff
border-color=#${ACCENT_PRIMARY}ff
border-size=2
border-radius=8
padding=12
margin=8
default-timeout=5000
max-visible=5
EOF
    makoctl reload 2>/dev/null || true
fi

# ── btop ──────────────────────────────────────────────────────────
if [[ "$(uname)" == "Linux" ]]; then
    BTOP_THEMES_DIR="$HOME/.config/btop/themes"
    mkdir -p "$BTOP_THEMES_DIR"
    if [ -f "$DOTFILES/btop/.config/btop/themes/$THEME.theme" ]; then
        cp "$DOTFILES/btop/.config/btop/themes/$THEME.theme" "$BTOP_THEMES_DIR/active.theme"
    fi
    BTOP_CONF="$HOME/.config/btop/btop.conf"
    if [ -f "$BTOP_CONF" ]; then
        if grep -q "^color_theme" "$BTOP_CONF"; then
            sed -i 's/^color_theme.*$/color_theme = "active"/' "$BTOP_CONF"
        else
            echo 'color_theme = "active"' >> "$BTOP_CONF"
        fi
    fi
fi

# ── Fuzzel app launcher ───────────────────────────────────────────
if [[ "$(uname)" == "Linux" ]]; then
    mkdir -p "$HOME/.config/fuzzel"
    cat > "$HOME/.config/fuzzel/fuzzel.ini" << EOF
[main]
font=JetBrainsMono Nerd Font:size=13
terminal=foot {cmd}
width=30
lines=8
tabs=2
horizontal-pad=20
vertical-pad=10
inner-pad=8

[colors]
background=${BG_DARK}ff
text=${FG_DIM}ff
match=${ACCENT_PRIMARY}ff
selection=${BG_LIGHT}cc
selection-text=${FG_BRIGHT}ff
border=${ACCENT_PRIMARY}ff

[border]
width=2
radius=8
EOF
    pkill fuzzel 2>/dev/null || true
fi

# ── Fastfetch keyColor + DU logo colors ──────────────────────────
FASTFETCH_CONF="$HOME/.config/fastfetch/config.jsonc"
if [ -f "$FASTFETCH_CONF" ]; then
    if [[ "$(uname)" == "Darwin" ]]; then
        sed -i '' "s/\"color\": \"#[0-9a-fA-F]*\"/\"color\": \"#${ACCENT_PRIMARY}\"/" "$FASTFETCH_CONF"
        sed -i '' "s/\"1\": \"#[0-9a-fA-F]*\"/\"1\": \"#${ACCENT_PRIMARY}\"/" "$FASTFETCH_CONF"
        sed -i '' "s/\"2\": \"#[0-9a-fA-F]*\"/\"2\": \"#${RED}\"/" "$FASTFETCH_CONF"
    else
        sed -i "s/\"color\": \"#[0-9a-fA-F]*\"/\"color\": \"#${ACCENT_PRIMARY}\"/" "$FASTFETCH_CONF"
        sed -i "s/\"1\": \"#[0-9a-fA-F]*\"/\"1\": \"#${ACCENT_PRIMARY}\"/" "$FASTFETCH_CONF"
        sed -i "s/\"2\": \"#[0-9a-fA-F]*\"/\"2\": \"#${RED}\"/" "$FASTFETCH_CONF"
    fi
fi

# ── GTK3/GTK4 (adw-gtk3 + libadwaita named colors, Gradience-style) ──
if [[ "$(uname)" == "Linux" ]]; then
    mkdir -p "$HOME/.config/gtk-3.0" "$HOME/.config/gtk-4.0"
    GTK_CSS="/* Generated by switch-theme.sh ($THEME_NAME) — DO NOT EDIT */
@define-color accent_color #${ACCENT_PRIMARY};
@define-color accent_bg_color #${ACCENT_PRIMARY};
@define-color accent_fg_color #${BG_DARK};
@define-color window_bg_color #${BG_DARK};
@define-color window_fg_color #${FG_BRIGHT};
@define-color view_bg_color #${BG_DARK};
@define-color view_fg_color #${FG_BRIGHT};
@define-color headerbar_bg_color #${BG_MID};
@define-color headerbar_fg_color #${FG_BRIGHT};
@define-color headerbar_backdrop_color #${BG_DARK};
@define-color headerbar_border_color #${BG_LIGHTER};
@define-color sidebar_bg_color #${BG_MID};
@define-color sidebar_fg_color #${FG_BRIGHT};
@define-color sidebar_backdrop_color #${BG_DARK};
@define-color secondary_sidebar_bg_color #${BG_MID};
@define-color secondary_sidebar_fg_color #${FG_BRIGHT};
@define-color card_bg_color #${BG_MID};
@define-color card_fg_color #${FG_BRIGHT};
@define-color dialog_bg_color #${BG_MID};
@define-color dialog_fg_color #${FG_BRIGHT};
@define-color popover_bg_color #${BG_MID};
@define-color popover_fg_color #${FG_BRIGHT};
@define-color destructive_bg_color #${RED};
@define-color destructive_fg_color #${BG_DARK};
@define-color destructive_color #${RED};
@define-color success_color #${GREEN};
@define-color warning_color #${YELLOW};
@define-color error_color #${RED};"
    echo "$GTK_CSS" > "$HOME/.config/gtk-3.0/gtk.css"
    echo "$GTK_CSS" > "$HOME/.config/gtk-4.0/gtk.css"
fi

# ── zsh prompt (p10k overrides, sourced from .zshrc after .p10k.zsh) ──
mkdir -p "$HOME/.config/zsh"
cat > "$HOME/.config/zsh/prompt-colors.zsh" << EOF
# Generated by switch-theme.sh ($THEME_NAME) — DO NOT EDIT
# Hex colors need a truecolor terminal (foot/ghostty: yes).
typeset -g POWERLEVEL9K_BACKGROUND='#${BG_MID}'
typeset -g POWERLEVEL9K_OS_ICON_FOREGROUND='#${FG_BRIGHT}'
typeset -g POWERLEVEL9K_DIR_FOREGROUND='#${ACCENT_PRIMARY}'
typeset -g POWERLEVEL9K_DIR_SHORTENED_FOREGROUND='#${FG_DIM}'
typeset -g POWERLEVEL9K_DIR_ANCHOR_FOREGROUND='#${ACCENT_PRIMARY}'
typeset -g POWERLEVEL9K_VCS_CLEAN_FOREGROUND='#${GREEN}'
typeset -g POWERLEVEL9K_VCS_UNTRACKED_FOREGROUND='#${GREEN}'
typeset -g POWERLEVEL9K_VCS_MODIFIED_FOREGROUND='#${YELLOW}'
typeset -g POWERLEVEL9K_PROMPT_CHAR_OK_{VIINS,VICMD,VIVIS,VIOWR}_FOREGROUND='#${GREEN}'
typeset -g POWERLEVEL9K_PROMPT_CHAR_ERROR_{VIINS,VICMD,VIVIS,VIOWR}_FOREGROUND='#${RED}'
typeset -g POWERLEVEL9K_STATUS_OK_FOREGROUND='#${GREEN}'
typeset -g POWERLEVEL9K_STATUS_OK_PIPE_FOREGROUND='#${GREEN}'
typeset -g POWERLEVEL9K_STATUS_ERROR_FOREGROUND='#${RED}'
typeset -g POWERLEVEL9K_STATUS_ERROR_SIGNAL_FOREGROUND='#${RED}'
typeset -g POWERLEVEL9K_STATUS_ERROR_PIPE_FOREGROUND='#${RED}'
typeset -g POWERLEVEL9K_COMMAND_EXECUTION_TIME_FOREGROUND='#${YELLOW}'
typeset -g POWERLEVEL9K_CONTEXT_FOREGROUND='#${FG_MID}'
typeset -g POWERLEVEL9K_CONTEXT_ROOT_FOREGROUND='#${RED}'
typeset -g POWERLEVEL9K_TIME_FOREGROUND='#${FG_DIM}'
typeset -g POWERLEVEL9K_MULTILINE_FIRST_PROMPT_GAP_FOREGROUND='#${BG_LIGHTER}'
EOF

# ── Greeter staging (applied separately: sudo sh scripts/greeter-apply.sh) ──
if [[ "$(uname)" == "Linux" ]]; then
    bash "$DOTFILES/scripts/stage-greeter-theme.sh" "$ACCENT_PRIMARY" || true
fi

# ── Save current theme ────────────────────────────────────────────
echo "$THEME" > "$HOME/.config/current-theme"

echo "✓ Switched to: $THEME_NAME"
echo "  borders reloaded · waybar restarted · foot updated · ghostty updated · hyprlock updated"
echo "  mako updated · btop theme updated · fuzzel updated · fastfetch updated"
