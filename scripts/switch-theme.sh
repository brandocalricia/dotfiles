#!/usr/bin/env bash
# Usage: bash ~/dotfiles/scripts/switch-theme.sh <theme>

set -e
THEME="$1"
DOTFILES="$HOME/dotfiles"
THEMES_DIR="$DOTFILES/themes"

if [ -z "$THEME" ]; then
    echo "Available: $(ls $THEMES_DIR/*.sh | xargs -n1 basename | sed 's/.sh//' | tr '\n' ' ')"
    exit 1
fi

if [ ! -f "$THEMES_DIR/$THEME.sh" ]; then
    echo "Theme '$THEME' not found."
    exit 1
fi

source "$THEMES_DIR/$THEME.sh"
echo "Switching to: $THEME_NAME"

# ── Hyprland border colors (sourced by hyprland.conf; never edits the tracked
#    config — see colors.conf.base / SETUP-NOTES) ───────────────────────────
if [[ "$(uname)" == "Linux" ]]; then
cat > "$HOME/.config/hypr/colors.conf" << EOF
# Generated by switch-theme.sh ($THEME_NAME) — DO NOT EDIT
\$border_active_1 = rgba(${BORDER_ACTIVE_1}ff)
\$border_active_2 = rgba(${BORDER_ACTIVE_2}ff)
\$border_inactive = rgba(${BORDER_INACTIVE}aa)
EOF
    hyprctl reload
fi

# ── Waybar CSS (hex only, no rgb conversion) ──────────────────────
if [[ "$(uname)" == "Linux" ]]; then
cat > "$HOME/.config/waybar/style.css" << EOF
* {
    font-family: "JetBrainsMono Nerd Font", "JetBrainsMono NF", "Font Awesome 6 Free", monospace;
    font-size: 13px;
    font-weight: 500;
    min-height: 0;
    border: none;
    border-radius: 0;
}
window#waybar {
    background: transparent;
    color: #${FG_DIM};
}
.modules-left, .modules-center, .modules-right {
    background: #${BAR_BG};
    border: 1px solid #${BAR_BORDER};
    border-radius: 10px;
    margin: 6px 4px;
    padding: 0 6px;
}
#workspaces { padding: 0 4px; }
/* base = occupied: bright chip so windows-elsewhere are visible at a glance */
#workspaces button {
    padding: 0 8px;
    margin: 4px 2px;
    color: #${FG_BRIGHT};
    background: #${BG_MID};
    border-radius: 6px;
    transition: all 0.2s ease;
}
/* empty persistent workspaces: dim, no chip */
#workspaces button.empty {
    color: #${BAR_INACTIVE_WS};
    background: transparent;
}
/* active must come after .empty: an empty focused workspace has both classes */
#workspaces button.active {
    color: #${BG_DARK};
    background: #${ACCENT_PRIMARY};
}
#workspaces button.urgent {
    color: #${BG_DARK};
    background: #${RED};
}
#workspaces button:hover {
    background: #${BG_LIGHT};
    color: #${FG_BRIGHT};
}
#clock {
    padding: 0 14px;
    color: #${ACCENT_PRIMARY};
    font-weight: 600;
}
#cpu, #memory, #disk, #network, #pulseaudio, #battery, #tray {
    padding: 0 10px;
    margin: 4px 3px;
    background: #${BG_MID};
    color: #${FG_DIM};
    border-radius: 6px;
    transition: all 0.2s ease;
}
#cpu:hover, #memory:hover, #disk:hover, #network:hover, #pulseaudio:hover, #battery:hover {
    background: #${BG_LIGHT};
}
#cpu { color: #${YELLOW}; }
#memory { color: #${ORANGE}; }
#disk { color: #${PURPLE}; }
#network { color: #${GREEN}; }
#pulseaudio { color: #${TEAL}; }
#pulseaudio.muted { color: #${RED}; }
#battery { color: #${ACCENT_PRIMARY}; }
#battery.warning { color: #${YELLOW}; }
#battery.critical { color: #${RED}; }
#tray { padding: 0 8px; }
tooltip {
    background: #${BG_DARK};
    border: 1px solid #${BG_LIGHTER};
    border-radius: 8px;
    color: #${FG_BRIGHT};
    padding: 4px;
}
EOF
fi

# ── foot terminal ─────────────────────────────────────────────────
if [[ "$(uname)" == "Linux" ]]; then
cat > "$HOME/.config/foot/foot.ini" << EOF
font=JetBrainsMono Nerd Font:size=11
pad=12x12
dpi-aware=yes

[cursor]
style=beam

[colors-dark]
alpha=0.95
background=${BG_DARK}
foreground=${FG_DIM}
selection-background=${BG_LIGHT}
selection-foreground=${FG_BRIGHT}
regular0=${BG_MID}
regular1=${RED}
regular2=${GREEN}
regular3=${YELLOW}
regular4=${ACCENT_SECONDARY}
regular5=${PURPLE}
regular6=${TEAL}
regular7=${FG_MID}
bright0=${BG_LIGHTER}
bright1=${RED}
bright2=${GREEN}
bright3=${YELLOW}
bright4=${ACCENT_PRIMARY}
bright5=${PURPLE}
bright6=${ACCENT_PRIMARY}
bright7=${FG_BRIGHT}
EOF
    # Scratchpad terminals bake colors at spawn — kill them so pyprland
    # respawns with the new foot.ini/btop theme on next toggle
    pkill -f '^foot --app-id=dropdown-' 2>/dev/null || true
fi

# ── Ghostty terminal (config statically references theme=active; we only write
#    the gitignored active palette file — like btop) ───────────────────────
GHOSTTY_THEMES_DIR="$HOME/.config/ghostty/themes"
mkdir -p "$GHOSTTY_THEMES_DIR"
cat > "$GHOSTTY_THEMES_DIR/active" << EOF
palette = 0=#${BG_MID}
palette = 1=#${RED}
palette = 2=#${GREEN}
palette = 3=#${YELLOW}
palette = 4=#${ACCENT_SECONDARY}
palette = 5=#${PURPLE}
palette = 6=#${TEAL}
palette = 7=#${FG_MID}
palette = 8=#${BG_LIGHTER}
palette = 9=#${RED}
palette = 10=#${GREEN}
palette = 11=#${YELLOW}
palette = 12=#${ACCENT_PRIMARY}
palette = 13=#${PURPLE}
palette = 14=#${ACCENT_PRIMARY}
palette = 15=#${FG_BRIGHT}
background = #${BG_DARK}
foreground = #${FG_DIM}
cursor-color = #${ACCENT_PRIMARY}
selection-background = #${BG_LIGHT}
selection-foreground = #${FG_BRIGHT}
EOF

# ── hyprlock ──────────────────────────────────────────────────────
if [[ "$(uname)" == "Linux" ]]; then
cat > "$HOME/.config/hypr/hyprlock.conf" << EOF
auth {
    fingerprint {
        enabled = true
    }
}
background {
    monitor =
    path = screenshot
    blur_passes = 3
    blur_size = 8
    contrast = 0.9
    brightness = 0.8
}
input-field {
    monitor =
    size = 280, 56
    outline_thickness = 2
    dots_size = 0.28
    dots_spacing = 0.35
    dots_center = true
    outer_color = rgba(${ACCENT_PRIMARY}ff)
    inner_color = rgba(${BG_DARK}b3)
    font_color = rgba(${FG_DIM}ff)
    fade_on_empty = false
    placeholder_text = <i>password...</i>
    hide_input = false
    rounding = 12
    check_color = rgba(${GREEN}ff)
    fail_color = rgba(${RED}ff)
    position = 0, -140
    halign = center
    valign = center
}
